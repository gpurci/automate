#include "minimizare.h"

char				reducere_term_si(char *c)
{
	int				i;
	int				j;

	i = 0;
	while (c[i])
	{
		j = i + 1;
		while (c[j])
		{
			if (c[i] == ~c[j])
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
