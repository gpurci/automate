#include "minimizare.h"

void				ft_del_nume_id(t_nume_id **term)
{
	size_t			i;

	i = 0;
	while ((*term)[i].id != -1)
	{
		ft_del_value_nume_id(&(*term)[i]);
		i++;
	}
	free(*term);
	*term = 0;
}
